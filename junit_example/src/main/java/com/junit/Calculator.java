//simple method to do mathematical calculations
public class Calculator {

    //multiply two int arguments
    public int multiply (int a, int b){

        return a*b;
    }
    //if given one argument squares argument
    public int multiply(int a){
        return a*a;
    }
    //divide second argument by first
    public int divide (int a, int b){

        return b/a;
    }
    //subtract second argument from first
    public int subtract (int a, int b){

        return b-a;
    }
    //add two arguments
    public int add(int a, int b){

        return a+b;
    }



}
